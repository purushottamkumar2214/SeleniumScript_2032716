package Java_Core_1;

public class continueProg {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++){  
	        if(i==5){  
	            //using continue statement 
	        	System.out.println("y");
	            continue;//it will skip the rest statement  
	        }  
	        System.out.println(i);  
	    }  

	}

}
